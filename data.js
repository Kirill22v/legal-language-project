    checkText: function(text) {
        const errors = [];
        const lowerText = text.toLowerCase();
        
        // Проверка некорректных слов
        for (const [badWord, suggestion] of Object.entries(this.incorrectWords)) {
            if (lowerText.includes(badWord)) {
                errors.push({
                    type: 'некорректное слово',
                    found: badWord,
                    suggestion: suggestion,
                    explanation: `Бытовое слово "${badWord}" лучше заменить на "${suggestion}"`
                });
            }
        }
        
        // Проверка стилевых ошибок (повторение слов)
        for (const errorWord of this.styleErrors) {
            const regex = new RegExp(errorWord, 'gi');
            const matches = text.match(regex);
            if (matches && matches.length > 2) {
                errors.push({
                    type: 'стилевая ошибка',
                    found: `Слово "${errorWord}" повторяется ${matches.length} раз`,
                    suggestion: 'Используйте синонимы или перефразируйте',
                    explanation: `Избегайте повторения слова "${errorWord}" в тексте`
                });
            }
        }
        
        return errors.slice(0, 10);
    }
